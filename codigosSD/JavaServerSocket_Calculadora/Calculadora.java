public class Calculadora {
	//classe onde os cálculos são feitos
	
    public double soma(double oper1, double oper2) {
        return oper1 + oper2; //realiza a operação de adição
    }
    public double sub(double oper1, double oper2) {
        return oper1 - oper2; //realiza a operação de subtração
    }
    public double div(double oper1, double oper2) {
        return oper1 / oper2; //realiza a operação de divisão
    }
    public double multi(double oper1, double oper2) {
        return oper1 * oper2; ////realiza a operação de multiplicação
    }
}